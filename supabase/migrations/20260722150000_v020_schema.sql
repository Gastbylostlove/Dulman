FOR SELECT TO authenticated USING (auth_user_id = auth.uid());
CREATE POLICY user_account_update_own ON user_account
FOR UPDATE TO authenticated USING (auth_user_id = auth.uid()) WITH CHECK (auth_user_id = auth.uid());

CREATE POLICY chat_select_participant ON chat
FOR SELECT TO authenticated USING (user_a_id = current_login_id() OR user_b_id = current_login_id());

CREATE POLICY message_select_participant ON message
FOR SELECT TO authenticated USING (
  EXISTS (
    SELECT 1 FROM chat
    WHERE chat.id = message.chat_id
      AND (chat.user_a_id = current_login_id() OR chat.user_b_id = current_login_id())
  )
);

CREATE POLICY media_select_participant ON media
FOR SELECT TO authenticated USING (
  EXISTS (
    SELECT 1 FROM message
    JOIN chat ON chat.id = message.chat_id
    WHERE message.id = media.message_id
      AND (chat.user_a_id = current_login_id() OR chat.user_b_id = current_login_id())
  )
);

CREATE POLICY reset_log_select_participant ON chat_reset_log
FOR SELECT TO authenticated USING (
  EXISTS (
    SELECT 1 FROM chat
    WHERE chat.id = chat_reset_log.chat_id
      AND (chat.user_a_id = current_login_id() OR chat.user_b_id = current_login_id())
  )
);

